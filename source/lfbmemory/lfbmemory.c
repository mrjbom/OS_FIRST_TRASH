uint32_t* LFBMEMORY_ADDR = NULL;

void init_lfb_mem() {
    LFBMEMORY_ADDR = (uint32_t*)((uint32_t)MBI->framebuffer_addr);
}

void clear_lfb_mem(uint32_t color) {
    for (uint32_t pixelPos = 0; pixelPos < MBI->framebuffer_width * MBI->framebuffer_height; pixelPos++)
        LFBMEMORY_ADDR[pixelPos] = color;
}

void write_pixel_lfb_mem(uint32_t x, uint32_t y, uint32_t color) {
    uint32_t pixelPos = x * MBI->framebuffer_width + y;
    LFBMEMORY_ADDR[pixelPos] = color;
}

void scale_symbol(uint32_t n_lines_map, uint32_t n_columns_map, bool map[][n_columns_map], uint32_t x_pos, uint32_t y_pos, uint32_t text_color, uint32_t back_color, uint32_t scale) {
    uint32_t tmp_counter = 0;
    for(uint32_t j = 0; j < n_lines_map; ++j)
    {
        int tmp_counter2 = 0;
        for(uint32_t i = 0; i < n_columns_map; ++i)
        {
            //dprintf("%I ", map[j][i]);
            for (uint32_t n = 0; n < scale; ++n)
			{
				for (uint32_t l = 0; l < scale; ++l)
				{
					//cout << "(" << j << "; " << i << ") => (" << n + tmpCounter << "; " << l + tmpCounter2 << ") ";
					//newMap[n + tmpCounter][l + tmpCounter2] = map[j][i];
                    //write_pixel_lfb_mem(n + tmp_counter, l + tmp_counter2, map[j][i]);
                    if(map[j][i])
                    {
                        write_pixel_lfb_mem(n + tmp_counter + y_pos, l + tmp_counter2 + x_pos, text_color);
                    }
                    else
                    {
                        write_pixel_lfb_mem(n + tmp_counter + y_pos, l + tmp_counter2 + x_pos, back_color);
                    }
                    
				}
			}
			tmp_counter2 += scale;
        }
        dprintf("\n");
        tmp_counter += scale;
    }
}

